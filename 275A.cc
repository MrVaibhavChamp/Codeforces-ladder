#include <iostream>
using namespace std;

int main()
{
    int a[3][3], b[3][3];
    for (int i = 0; i < 3; i++)
    {
        for (int j = 0; j < 3; j++)
        {
            a[i][j] = 1;
        }
    }

    for (int i = 0; i < 3; i++)
    {
        for (int j = 0; j < 3; j++)
        {
            cin >> b[i][j];
        }
    }
    
    if ((b[0][0]+b[0][1]+b[1][0])%2!=0)
    {
        a[0][0]=0;
    }
    
    if ((b[0][2]+b[0][1]+b[1][2])%2!=0)
    {
        a[0][2]=0;
    }
    
    if ((b[2][0]+b[1][0]+b[2][1])%2!=0)
    {
        a[2][0]=0;
    }
    
    if ((b[2][2]+b[2][1]+b[1][2])%2!=0)
    {
        a[2][2]=0;
    }
    
    if ((b[0][1]+b[0][0]+b[1][1]+b[0][2])%2!=0)
    {
        a[0][1]=0;
    }
    
    if ((b[1][0]+b[0][0]+b[1][1]+b[2][0])%2!=0)
    {
        a[1][0]=0;
    }
    
    if ((b[1][2]+b[0][2]+b[1][1]+b[2][2])%2!=0)
    {
        a[1][2]=0;
    }
    
    if ((b[2][1]+b[2][0]+b[1][1]+b[2][2])%2!=0)
    {
        a[2][1]=0;
    }
    
    if ((b[1][1]+b[0][1]+b[1][0]+b[1][2]+b[2][1])%2!=0)
    {
        a[1][1]=0;
    }
    
    for (int i = 0; i < 3; i++)
    {
        for (int j = 0; j < 3; j++)
        {
            cout << a[i][j];
        }
        cout << endl;
    }

    return 0;
}